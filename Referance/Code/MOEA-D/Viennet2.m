function z = Viennet2(x)
    f1 = @(x, y) 0.5 .* (x - 2).^2 + (1/13) .* (y + 1).^2 + 3;
    f2 = @(x, y) (1/36) .* (x + y - 3).^2 + (1/8) .* (-x + y + 2).^2 - 17;
    f3 = @(x, y) (1/175) .* (x + 2 .* y - 1).^2 + (1/17) .* (2 .* y - x).^2 - 13;
    z = [f1(x(1, :), x(2, :)), f2(x(1, :), x(2, :)), f3(x(1, :), x(2, :))];
end
